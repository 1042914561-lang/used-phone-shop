// 按年份排序的所有机型（用于按年份筛选）
export const ALL_YEARS: number[] = (() => {
  const years = new Set<number>();
  for (const brand of PHONE_BRANDS) {
    for (const model of brand.models) {
      if (model.releaseYear > 0) years.add(model.releaseYear);
    }
  }
  return Array.from(years).sort((a, b) => b - a);
})();

// 全部热门机型（高维修频率）
export function getPopularModels(): { brand: PhoneBrand; model: PhoneModel }[] {
  const result: { brand: PhoneBrand; model: PhoneModel }[] = [];
  for (const brand of PHONE_BRANDS) {
    for (const model of brand.models) {
      if (model.popular) {
        result.push({ brand, model });
      }
    }
  }
  return result.sort((a, b) => {
    const ai = PHONE_BRANDS.findIndex((x) => x.key === a.brand.key);
    const bi = PHONE_BRANDS.findIndex((x) => x.key === b.brand.key);
    if (ai !== bi) return ai - bi;
    return b.model.releaseYear - a.model.releaseYear;
  });
}

// 搜索
export interface SearchResult {
  brand: PhoneBrand;
  model: PhoneModel;
}

export function searchPhones(q: string): SearchResult[] {
  const query = q.trim().toLowerCase();
  if (!query) return [];
  const results: SearchResult[] = [];
  for (const brand of PHONE_BRANDS) {
    const brandMatch =
      brand.name.toLowerCase().includes(query) ||
      (brand.aliases || []).some((a) => a.toLowerCase().includes(query));
    for (const model of brand.models) {
      if (model.releaseYear === 0) continue;
      const modelMatch = model.name.toLowerCase().includes(query);
      if (brandMatch || modelMatch) {
        results.push({ brand, model });
      }
    }
  }
  return results.sort((a, b) => b.model.releaseYear - a.model.releaseYear);
}

// 按品牌和年份筛选
export function getModelsByBrandAndYear(brandKey: string, year?: number): SearchResult[] {
  const brand = PHONE_BRANDS.find((b) => b.key === brandKey);
  if (!brand) return [];
  let models = brand.models.filter((m) => m.releaseYear > 0);
  if (year) models = models.filter((m) => m.releaseYear === year);
  return models
    .sort((a, b) => b.releaseYear - a.releaseYear)
    .map((model) => ({ brand, model }));
}
