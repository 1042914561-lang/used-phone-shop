export interface PhoneModel {
  name: string;
  releaseYear: number;
  popular?: boolean; // 热门机型（高维修频率）
  series?: string;
}

export interface PhoneBrand {
  key: string;
  name: string;
  aliases?: string[];
  models: PhoneModel[];
}

// =============== 辅助函数 ===============

/** 简洁地生成同系列下多款机型（避免大量重复书写） */
function m(name: string, year: number, popular = false, series?: string): PhoneModel {
  return { name, releaseYear: year, popular, series: series ?? name.split(/\s+/)[0] };
}

function arr(...models: PhoneModel[]): PhoneModel[] {
  return models;
}

